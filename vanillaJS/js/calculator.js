const calculator = {
    add: function(a, b) {
        console.log(a + b);
    },
    minus: function(a, b) {
        console.log(a - b);
    },
    div: function(a, b) {
        console.log(a / b);
    },
    pow: function(a, b) {
        console.log(a ** b);
    }
};

calculator.add(5, 1);
calculator.minus(5, 1);
calculator.div(5, 1);
calculator.pow(5, 1);